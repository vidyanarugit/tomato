package com.tomato.spring.bean;

public class SearchCriteria {
	String location;

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}
	
	double foodid;
	
	double servid;
	
	double ambid;

	public double getFoodid() {
		return foodid;
	}

	public void setFoodid(double foodid) {
		this.foodid = foodid;
	}

	public double getServid() {
		return servid;
	}

	public void setServid(double servid) {
		this.servid = servid;
	}

	public double getAmbid() {
		return ambid;
	}

	public void setAmbid(double ambid) {
		this.ambid = ambid;
	}

}
